# Functions and Reuse
# Functions let you name a group of steps and use it again.

def print_session(title, minutes):
    print(title)
    print("Minutes:", minutes)
    print("Nice. That session is recorded.")
    print()

print_session("Python review", 20)
print_session("Project practice", 30)

