# Variables
# A variable is a name that stores a value.
# You can use variables to remember information and use it later.

# To run this file in the terminal, use the command: python3 04_variables

# This variable stores text.
name = "Alex"
print(name)

# This variable stores a number.
age = 14
print(age)

# Variable names can describe what the value means.
favourite_food = "pizza"
print(favourite_food)

# You can print text and variables near each other.
print("My name is:")
print(name)

# Variables can change.
mood = "happy"
print(mood)

mood = "excited"
print(mood)

