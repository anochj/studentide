# Numbers and Math
# Python can work with numbers and do math.

# To run this file in the terminal, use the command: python3 06_numbers_and_math

# Numbers do not need quotes.
print(5)
print(100)

# Basic math.
print(2 + 3)
print(10 - 4)
print(6 * 7)
print(20 / 5)

# You can store numbers in variables.
score = 10
bonus = 5
total_score = score + bonus

print()
print("Score:")
print(score)
print("Bonus:")
print(bonus)
print("Total score:")
print(total_score)

# Parentheses can make the order clear.
print()
print(2 + 3 * 4)
print((2 + 3) * 4)

# Text numbers and real numbers are different.
print()
print("5" + "5")
print(5 + 5)

