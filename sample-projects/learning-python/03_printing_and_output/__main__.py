# Printing and Output
# In this module, you will practice using print() to show output in the console.
# Output is the information your program shows after it runs.

# To run this file in the terminal, use the command: python3 03_printing_and_output

# print() displays text in the console.
print("Welcome to Module 3.")

# You can print more than one line.
print("Python can show one message at a time.")
print("Each print() usually starts a new line.")

# Text goes inside quotes.
print("This is a line of text.")

# Numbers can be printed too.
print(42)
print(100)

# You can use an empty print() to make a blank line.
print()
print("The blank line above made this easier to read.")

