# Python Syntax
# Syntax is the set of rules Python follows so it can understand your code.
# Think of it like grammar in English.
# If the grammar is messy, the sentence feels broken.
# If the syntax is messy, Python gets confused too.

# To run this file in the terminal, use the command: python3 02_python_syntax

# print() is a function.
# A function is a built in tool that tells Python to do something.
# print() tells Python to show something in the console.
print("Welcome back to Python syntax!")

# Text in Python goes inside quotes.
# Double quotes work:
print("This sentence uses double quotes.")

# Single quotes work too:
print('This one uses single quotes.')

# Comments are ignored by Python, so they are great for leaving notes.
print("Comments are for humans, not for Python.")  # This is an inline comment

# Tiny symbols matter.
# The brackets in print() are part of the syntax.
print("If you forget a bracket or quote, Python will complain.")

# Indentation means spaces at the start of a line.
# You do not need to fully understand if statements yet.
# For now, just notice that the line below is pushed to the right.
if True:
    print("This line is indented, and that indentation matters.")

